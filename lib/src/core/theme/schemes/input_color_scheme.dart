import 'dart:ui';

class InputColorScheme {
  final Color enabledOutline;
  final Color disabledOutline;
  final Color focusedOutline;
  final Color errorOutline;
  final Color fill;

  const InputColorScheme({
    required this.enabledOutline,
    required this.disabledOutline,
    required this.focusedOutline,
    required this.errorOutline,
    required this.fill,
  });
}
